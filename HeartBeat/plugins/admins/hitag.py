@app.on_message(filters.command(["ajtag" ], prefixes=["/", "@", "#"]))
async def mentionall(client, message):
    chat_id = message.chat.id
    if message.chat.type == ChatType.PRIVATE:
        return await message.reply("à¹ á´›ÊœÉªs á´„á´á´á´á´€É´á´… á´É´ÊŸÊ Ò“á´Ê€ É¢Ê€á´á´œá´˜s.")

    is_admin = False
    try:
        participant = await client.get_chat_member(chat_id, message.from_user.id)
    except UserNotParticipant:
        is_admin = False
    else:
        if participant.status in (
            ChatMemberStatus.ADMINISTRATOR,
            ChatMemberStatus.OWNER
        ):
            is_admin = True
    if not is_admin:
        return await message.reply("à¹ Êá´á´œ á´€Ê€á´‡ É´á´á´› á´€á´…á´ÉªÉ´ Ê™á´€Ê™Ê, á´É´ÊŸÊ á´€á´…á´ÉªÉ´s á´„á´€É´ á´›á´€É¢ á´á´‡á´Ê™á´‡Ê€s. ")

    if message.reply_to_message and message.text:
        return await message.reply("/ajtag á´„á´€É´ á´›á´€É¢ á´á´‡á´Ê™á´‡Ê€s...")
    elif message.text:
        mode = "text_on_cmd"
        msg = message.text
    elif message.reply_to_message:
        mode = "text_on_reply"
        msg = message.reply_to_message
        if not msg:
            return await message.reply("/ajtag á´„á´€É´ á´›á´€É¢ á´á´‡á´Ê™á´‡Ê€s...")
    else:
        return await message.reply("/ajtag á´„á´€É´ á´›á´€É¢ á´á´‡á´Ê™á´‡Ê€s...")
    if chat_id in spam_chats:
        return await message.reply("à¹ á´˜ÊŸá´‡á´€sá´‡ á´€á´› Ò“ÉªÊ€sá´› sá´›á´á´˜ Ê€á´œÉ´É´ÉªÉ´É¢ á´á´‡É´á´›Éªá´É´ á´˜Ê€á´á´„á´‡ss...")
    spam_chats.append(chat_id)
    usrnum = 0
    usrtxt = ""
    async for usr in client.get_chat_members(chat_id):
        if not chat_id in spam_chats:
            break
        if usr.user.is_bot:
            continue
        usrnum += 1
        usrtxt += f"<blockquote>[{usr.user.first_name}](tg://user?id={usr.user.id}</blockquote>\n) "

        if usrnum == 1:
            if mode == "text_on_cmd":
                txt = f"<blockquote>{usrtxt} {random.choice(TAGMES)}</blockquote>"
                await client.send_message(chat_id, txt)
            elif mode == "text_on_reply":
                await msg.reply(f"[{random.choice(EMOJI)}](tg://user?id={usr.user.id})")
            await asyncio.sleep(3)
            usrnum = 0
            usrtxt = ""
    try:
        spam_chats.remove(chat_id)
    except:
        pass


